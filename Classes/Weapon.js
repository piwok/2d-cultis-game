class Weapon {
    constructor(width, height, color, damage, duration, rel_pos_x, rel_pos_y) {
        this.width = width;
        this.height = height;
        this.color = color;
        this.damage = damage;
        this.duration = duration;
        this.rel_pos_x = rel_pos_x;
        this.rel_pos_y = rel_pos_y;
    }
    
}